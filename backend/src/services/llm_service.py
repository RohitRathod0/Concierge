def generate_response_agentic(
    message: str,
    history: List[Dict[str, str]] = None,
    user_profile: Optional[Dict] = None,
    page_context: Optional[str] = None,
) -> Dict[str, Any]:
    """
    The main agentic response generator.
    Returns a dict with 'answer' (str) and 'cross_sell' (dict).
    """
    model = get_gemini_model()
    if not model:
        logger.warning("Gemini model unavailable. Using local agentic fallback.")
        return _local_agentic_fallback(message, user_profile, page_context)

    system_prompt = _build_system_prompt(user_profile, page_context)
    history_text = _format_history(history)

    try:
        full_prompt = (
            f"{system_prompt}\n\n"
            f"RECENT CONVERSATION:\n{history_text}\n\n"
            f"CURRENT USER MESSAGE:\n{message}\n\n"
            f"Respond ONLY with valid JSON."
        )
        response = _run_model_call(lambda: model.generate_content(full_prompt))
        if response is None:
            return _local_agentic_fallback(message, user_profile, page_context)
        
        raw = response.text.strip()
        # Strip markdown code fences if present
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        raw = raw.strip().rstrip("```").strip()
        
        result = json.loads(raw)
        return {
            "answer": result.get("answer", raw),
            "cross_sell": result.get("cross_sell"),
        }
    except json.JSONDecodeError:
        # LLM didn't return clean JSON — extract what we can
        try:
            answer = response.text.strip()
            return {"answer": answer, "cross_sell": None}
        except Exception:
            return _local_agentic_fallback(message, user_profile, page_context)
    except Exception as e:
        logger.error(f"Gemini agentic response failed: {e}", exc_info=True)
        return _local_agentic_fallback(message, user_profile, page_context)


