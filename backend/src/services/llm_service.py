def classify_intent(message: str) -> str:
    """Classify the user intent into predefined buckets using Gemini."""
    message_lower = (message or "").lower()
    if "course" in message_lower or "masterclass" in message_lower:
        return "course_query"
    if "ipo" in message_lower:
        return "ipo_query"
    if "tax" in message_lower or "80c" in message_lower or "elss" in message_lower:
        return "tax_query"
    if "portfolio" in message_lower:
        return "portfolio_query"
    if "stock" in message_lower or "stocks" in message_lower or "market" in message_lower:
        return "stock_query"
    if "sip" in message_lower or "mutual fund" in message_lower:
        return "mutual_fund_query"

    model = get_gemini_model()
    if not model:
        return "general_question"
    try:
        prompt = (
            f"Classify this user message into EXACTLY ONE of these categories: "
            f"learning, investing, stock_query, ipo_query, tax_query, mutual_fund_query, "
            f"course_query, service_inquiry, portfolio_query, general_question. "
            f"Message: \"{message}\". "
            f"Reply with ONLY the category name, nothing else."
        )
        response = _run_model_call(lambda: model.generate_content(prompt), timeout_seconds=8)
        if response is None:
            return "general_question"
        intent = response.text.strip().lower().split()[0]
        valid = ["learning", "investing", "stock_query", "ipo_query", "tax_query",
                 "mutual_fund_query", "course_query", "service_inquiry", "portfolio_query", "general_question"]
        return intent if intent in valid else "general_question"
    except Exception:
        return "general_question"
