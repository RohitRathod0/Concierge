def _get_next_ipo_summary() -> Optional[Dict[str, str]]:
    db = SessionLocal()
    try:
        ipo = (
            db.query(IPO)
            .filter(IPO.status.in_(["open", "upcoming"]))
            .order_by(IPO.open_date.asc())
            .first()
        )
        if not ipo:
            return None

        open_date = ipo.open_date.isoformat() if getattr(ipo, "open_date", None) else "date to be announced"
        price_band = None
        if ipo.price_band_low and ipo.price_band_high:
            price_band = f"₹{ipo.price_band_low}-₹{ipo.price_band_high}"

        return {
            "company_name": ipo.company_name,
            "open_date": open_date,
            "price_band": price_band or "price band awaited",
            "gmp": f"{float(ipo.gmp_percent):.0f}% GMP" if ipo.gmp_percent is not None else "GMP not available",
            "status": ipo.status,
        }
    except Exception as e:
        logger.warning("Unable to load upcoming IPO context: %s", e)
        return None
    finally:
        db.close()


