def _format_history(history: Optional[List[Dict[str, str]]]) -> str:
    if not history:
        return "No prior conversation."

    snippets = []
    for msg in history[-8:]:
        role = "User" if msg.get("role") == "user" else "Assistant"
        snippets.append(f"{role}: {msg.get('content', '')}")
    return "\n".join(snippets)


def _run_model_call(fn, timeout_seconds: int = 12):
    executor = ThreadPoolExecutor(max_workers=1)
    future = executor.submit(fn)
    try:
        result = future.result(timeout=timeout_seconds)
        executor.shutdown(wait=False, cancel_futures=False)
        return result
    except FuturesTimeoutError:
        logger.warning("Gemini call timed out after %ss", timeout_seconds)
        future.cancel()
        executor.shutdown(wait=False, cancel_futures=True)
        return None
    except Exception:
        executor.shutdown(wait=False, cancel_futures=True)
        raise


